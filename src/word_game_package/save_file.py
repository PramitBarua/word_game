import os
import pickle

def saving_file(num_dict):
    location = os.path.realpath(os.path.join(os.getcwd(), os.path.dirname(__file__)))
    #removing last 2 folders from location variable
    location = location[:location.find('src')] 
    with open(os.path.join(location,'word_list.pickle'), 'wb') as handle:
        pickle.dump(num_dict, handle, protocol=pickle.HIGHEST_PROTOCOL)
        
